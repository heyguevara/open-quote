				<h3>Open Source Initiative</h3>
				<p><a href="http://www.opensource.org/docs/definition.php"> <img src="http://www.opensource.org/trademarks/opensource/web/opensource-110x95.png" alt="Open Source (OSI) Logo" border="0" width="110" height="95"  class="float-left" ></a></p>

				<p>The OSI are the stewards of the <a href="http://www.opensource.org/docs/osd" mce_href="http://www.opensource.org/docs/osd" target="_blank">Open Source Definition (OSD)</a> and the community-recognized body for reviewing and approving licenses as OSD-conformant.</p>
				<p>The OSI is actively involved in Open Source community-building and education.  <a href="http://www.opensource.org/board" mce_href="http://www.opensource.org/board" target="_blank">OSI Board</a> members frequently travel the world to attend Open Source conferences and events, meet with open source developers and users, and to discuss with executives from the public and private sectors about how Open Source technologies, licenses, and models of development can provide economic and strategic advantages.</p>
 
