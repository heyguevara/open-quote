				<h1>History</h1>
							
				<p>OpenQuote has been donated to the open source community by <a href="http://www.appliedindustriallogic.com/">Applied Industrial Logic Limited</a>, a commercial organisation set up by a team of people who have been developing software solutions for insurance companies over many years. Originally known as MultiQuote (re-branded for the move to open source), the system was designed to enable the delivery of insurance products to market as quickly as possible.  This is achieved by allowing Underwriters, Brokers and Agents to design, build, test and release insurance products for any line of business with minimal support from IT.</p>
				<p>Currently OpenQuote supports the product design and quotation phase of the insurance life cycle, as development continues we expect other areas of the life cycle (policy administration, claims etc.) to be supported.</p>
				
