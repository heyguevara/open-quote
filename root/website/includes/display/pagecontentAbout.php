				<h1>About</h1>
				
			
				<h3>OpenQuote</h3>
				<p>OpenQuote is an <a href="opensource.php">open source</a> system designed to produce insurance quotations  using the Internet.  OpenQuote can be configured to work for any line of business, it uses business rules (risk evaluation and rates) that can be set up in a Microsoft Excel spreadsheet.  Many different insurance products can be run and controlled from OpenQuote at any one time.</p>
				<p>The system can be made available to brokers and proposers, quotes can be made and saved for the broker or proposer to return to at a later date. OpenQuote can issue quotation documentation to the broker or proposer and keep the Underwriter informed about all quotations and when they are accepted.</p>
				<p>An insurance product is configured by specifying the information that is required to be captured from the proposer, this configuration is then used by OpenQuote to automatically generate all the required screens. Basic  products predefined for various lines of business can be extended (used as a basis for) by new products when they are created, this speeds up time to market because less configuration is required. Any existing product can be extended this way by future products, with the new products inheriting all of the original product rules and attributes.</p>
				<p>Processed quotations and accepted quotations can be packaged up as an xml message for policy administration systems to use, this provides a mechanism to help automate transition from quote to policy.</p>
								
